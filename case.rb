fav_color = 'purple'

case fav_color
	when 'red' 
    puts "Red like fire"
	when 'orange' 
    puts "Orange like, well... an orange"
	when 'yellow' 
    puts"Yellow daffodils are so pretty in the spring!"
	when 'green' 
    puts "Have you been to the Emerald City in Oz?"
	when 'blue' 
    puts "Blue like the sky!"
	when 'purple' 
    puts "Purple plums are the tastiest."
	else 
    puts "Hmm, well I don't know what that color is!"
end

weather = "afe"

case weather 
  when 'chilly'
    puts "Wear a sweater."
  when 'raining'
    puts "Don't forget your galoushes. Splashing in puddles is fun."
  when 'sunny'
    puts "Yay, time for flip flops."
  when 'snowing'
    puts "Brr. Be sure to bundle up."
  when 'hot'
    puts "Only need shorts today."
  else 
    puts "Dressing in layers always works."
end